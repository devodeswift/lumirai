import Foundation
import SwiftUI

enum AppFonts{
    
    static func playFairDisplayReg(size: CGFloat) -> Font {
        return .custom("Playfair Display", size: size)
    }
    
    static func nunito(size: CGFloat) -> Font {
        return .custom("Nunito", size: size)
    }
}
